public class TiposVariaveis {
    public static void main(String[] args) {
        // Tipos primitivos
        int idade = 25; // Inteiro
        double salario = 4500.99; // Número decimal
        float altura = 1.75f; // Número decimal menor precisão
        char inicial = 'J'; // Caractere único
        boolean ativo = true; // Verdadeiro ou falso

        // Tipos não primitivos
        String nome = "João"; // Cadeia de caracteres

        // Exemplo de constantes
        final double PI = 3.14159; // Constante, valor não pode ser alterado

        // Exibindo os valores
        System.out.println("Idade: " + idade);
        System.out.println("Salário: " + salario);
        System.out.println("Altura: " + altura);
        System.out.println("Inicial: " + inicial);
        System.out.println("Ativo: " + ativo);
        System.out.println("Nome: " + nome);
        System.out.println("PI: " + PI);
    }
}